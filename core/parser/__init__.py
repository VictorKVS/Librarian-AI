## core.parser package
#
# Модули для парсинга и предобработки данных:
#   chunker.py — разбивка входного текста на логические чанки (500–1000 токенов)
#   loader.py  — определение типа файла (PDF, DOCX, TXT и т.д.) и чтение потока для дальнейшей обработки
#   parser.py  — токенизация текста, фильтрация стоп-слов, поддержка разных языков и базовая очистка